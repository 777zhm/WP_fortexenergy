					<li>в установленому порядку розглядати запити споживачів щодо діяльності, пов’язаної з постачанням природного газу;</li>
					<li>своєчасно надавати споживачу достовірну інформацію, у тому числі передбачену Законом України «Про особливості доступу до інформації у сферах постачання електричної енергії, природного газу, теплопостачання, централізованого постачання гарячої води, централізованого питного водопостачання та водовідведення», а також дані про фактичні нарахування (обсяг та вартість) за послуги з газопостачання, щоб дати можливість споживачеві регулювати власне споживання природного газу. Заходи щодо надання інформації для споживача мають бути передбачені у договорі постачання природного газу. Жодні додаткові витрати за надання інформації споживачем не оплачуються;</li>
					<li>своєчасно повідомляти споживача, якщо постачальник перебуває у процесі ліквідації, або визнання банкрутом, або постачальник проходить по процедурі призупинення/анулювання ліцензії на постачання природного газу, або його ліцензію на провадження діяльності з постачання природного газу анульовано, або її дію зупинено та про відсутність ресурсу природного газу;</li>
					<li>надати споживачеві остаточний рахунок (рахунок-фактуру) після зміни постачальника або розірвання договору постачання природного газу не пізніше ніж через шість тижнів після такої зміни або розірвання договору;</li>
					<li>виконувати інші обов’язки, передбачені <a target="_b" href="http://zakon3.rada.gov.ua/laws/show/z1382-15">Правилами постачання природного газу</a> та чинним законодавством.</li>
