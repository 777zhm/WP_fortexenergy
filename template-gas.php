				<h3>Умови постачання природного газу</h3>
				<p>Постачання природного газу здійснюється відповідно  до договору, за яким постачальник забов&#8217;язується поставити споживачеві природний газ належної якості та кількості у порядку, передбаченому договором, а споживач забов&#8217;язується оплатити вартість прийнятого природного газу в розмірі, строки та порядку, передбачених договором.</p>
				<p>Якість та інші фізико-хімічні характеристики природного газу визначаються згідно із встановленими стандартами та нормативно-правовими актами.</p>
				<p>Постачання природного газу здійснюється за цінами, що вільно встановлюються між постачальником та споживачем, крім випадків, передбачених Законом України <a href="http://zakon2.rada.gov.ua/laws/show/329-19">&#8220;Про ринок природного газу&#8221;</a></p>
			</div>
			<div class="col-lg-12">
				<h3>Права та обов&#8217;язки Споживача</h3>
				<p>Права споживача:</p>
				<ul class="docs_list">
					<li>на отримання природного газу в обсягах, визначених договором постачання природного газу, за умови дотримання його умов;</li>
					<li>на одночасне укладення декількох договорів постачання природного газу, якщо його річний обсяг споживання природного газу перевищує 30 млн куб. метрів;</li>
					<li>на безкоштовне отримання інформації щодо цін постачальника на природний газ та порядку оплати;</li>
					<li>самостійно припиняти (обмежувати) відбір природного газу для власних потреб з дотриманням вимог чинного законодавства, про що повинен письмово повідомляти всіх суб’єктів ринку природного газу, з якими укладено відповідні договори;</li>
					<li>вимагати поновлення постачання природного газу в установленому порядку після усунення порушень і компенсації оплати послуг за відключення та підключення, якщо припинення газопостачання відбулося без розірвання договору постачання природного газу;</li>
					<li>інші права, передбачені <a target="_b" href="http://zakon3.rada.gov.ua/laws/show/z1382-15">Правилами постачання природного газу</a> та чинним законодавством.</li>
				</ul>
				<p>Обов&#8217;язки споживача:</p>
				<ul class="docs_list">
					<li>дотримуватись вимог цих Правил;</li>
					<li>забезпечувати дотримання дисципліни відбору (споживання) природного газу в обсягах та на умовах, визначених договорами;</li>
					<li>своєчасно та в повному обсязі сплачувати за поставлений природний газ на умовах, визначених договорами;</li>
					<li>здійснювати комплекс заходів, спрямованих на запобігання виникненню загрози життю або травматизму, пошкодженню обладнання та продукції, негативних екологічних наслідків тощо в разі отримання повідомлення про припинення (обмеження) постачання (розподілу/транспортування) природного газу;</li>
					<li>забезпечувати допуск представників постачальника за пред’явленням службового посвідчення на територію власних об’єктів для звірки даних фактичного споживання природного газу;</li>
					<li>самостійно обмежувати (припиняти) споживання природного газу у випадках:
						<ul>
							<li>порушення строків оплати за договором на постачання природного газу; відсутності або недостатності підтвердженого обсягу природного газу, виділеного споживачу;</li>
							<li>перевитрат добової норми та/або місячного підтвердженого обсягу природного газу без узгодження з постачальником;</li>
							<li>відсутності укладеного договору постачання природного газу;</li>
							<li>інших випадках, передбачених <a target="_b" href="http://zakon3.rada.gov.ua/laws/show/z1382-15">Правилами постачання природного газу</a> та чинним законодавством.</li>
